export class Movie {
    constructor(id, title, description, releaseDate, rating, poster, backdrop, genres, duration, budget, originalTitle, productionCompanies) {
      this.id = id
      this.title = title
      this.description = description
      this.releaseDate = releaseDate
      this.rating = rating
      this.poster = poster
      this.backdrop = backdrop
      this.genres = genres
      this.duration = duration
      this.budget = budget
      this.originalTitle = originalTitle
      this.productionCompanies = productionCompanies
    }
  }